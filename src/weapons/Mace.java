package weapons;

public class Mace {
	private String name = "Massue";
	private int strength = 3;
	
	public Mace(){
		
	}

	//GETTER
	public String getName() {
		return name;
	}
	
	public int getStrength() {
		return strength;
	}

	
	//SETTER
	public void setName(String name) {
		this.name = name;
	}

	public void setStrength(int strength) {
		this.strength = strength;
	}
	
	
}
