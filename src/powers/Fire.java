package powers;

public class Fire {
	private String name = "Boule de feu";
	private int strength = 7;
	
	public Fire() {
		
	}

	
	//SETTER
	public String getName() {
		return name;
	}
	
	public int getStrength() {
		return strength;
	}
	
	
	//SETTER

	public void setName(String name) {
		this.name = name;
	}

	public void setStrength(int strength) {
		this.strength = strength;
	}
	
	
}
