package enemies;

public class Sorcier {
	private int health = 9;
	private int strength = 2;
	
	public Sorcier() {
		
	}

	
	//GETTER
	public int getHealth() {
		return health;
	}
	public int getStrength() {
		return strength;
	}

	
	//SETTER
	public void setHealth(int health) {
		this.health = health;
	}
	public void setStrength(int strength) {
		this.strength = strength;
	}
	
	
}
