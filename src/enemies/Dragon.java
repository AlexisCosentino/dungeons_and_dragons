package enemies;

public class Dragon {
	private int health = 15;
	private int strength = 4;
	
	public Dragon() {
		
	}

	
	//GETTER
	public int getHealth() {
		return health;
	}
	public int getStrength() {
		return strength;
	}

	
	//SETTER
	public void setHealth(int health) {
		this.health = health;
	}
	public void setStrength(int strength) {
		this.strength = strength;
	}

}
