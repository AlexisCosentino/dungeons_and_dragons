package enemies;

public class Gobelin {
	private int health = 6;
	private int strength = 1;
	
	public Gobelin() {
		
	}

	
	//GETTER
	public int getHealth() {
		return health;
	}
	public int getStrength() {
		return strength;
	}

	
	//SETTER
	public void setHealth(int health) {
		this.health = health;
	}
	public void setStrength(int strength) {
		this.strength = strength;
	}
}
